/** Filter and shuffle questions based on test configuration */
export function getFilteredQuestions(
    role: string,
    language: string,
    difficulty: string,
    count: number
) {
    const primary = QUESTION_BANK.filter((q) => {
        const roleMatch = q.role.includes(role as never) || q.role.includes('general');
        const langMatch = !q.language || q.language === language || q.language === 'general' || language === 'general';
        const diffMatch = q.difficulty === difficulty;
        return roleMatch && langMatch && diffMatch;
    });

    let pool = primary.length >= count
        ? primary
        : [
            ...primary,
            ...QUESTION_BANK.filter(
                (q) =>
                    !primary.includes(q) &&
                    (q.role.includes(role as never) || q.role.includes('general'))
            ),
        ];

    return [...pool].sort(() => Math.random() - 0.5).slice(0, count);
}
